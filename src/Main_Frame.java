    /**
     * Devuelve la solucion al puzzle seleccionado por el usuario, por el metodo elegido
     * @param g GestorSolucion con el puzzle elegido 
     * @param algoritmo metodo a resolver el puzzle elegido
     * @return true en caso de haber hallado una solucion al puzzle, false en caso contrario
     */
    private boolean getSolucion(GestorSolucion g, String algoritmo, double pesoHeuristica, double pesoCoste) {

    	boolean fin = false;
    	
    	if(algoritmo.equals(jRadioButton1.getText())) fin = g.escaladaSimple();
    	if(algoritmo.equals(jRadioButton2.getText())) fin = g.escaladaMaximaPendiente();
    	if(algoritmo.equals(jRadioButton3.getText())) fin = g.algoritmoA(pesoHeuristica,pesoCoste);
    	if(algoritmo.equals(jRadioButton4.getText())) fin = g.escaladaMaximaPendiente1();
    	
    	return fin;

    }
